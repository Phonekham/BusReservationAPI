import mongoose from 'mongoose';
const Schema = mongoose.Schema;

var memberSchema = new Schema({
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
});

const Member = mongoose.model('Member', memberSchema);

export default Member;
